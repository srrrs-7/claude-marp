SKIP_RE = re.compile(
    r"(アジェンダ|目次|おわりに|ありがとう|参考文献|Agenda|Introduction|References|TOC)"
    r"(\s*[\(\（].*[\)\）])?$",
    re.IGNORECASE,
)


def needs_subtitle(slide):
    layout = slide.get("layout") or "default"
    if layout not in ("default", ""):
        return False
    if len(slide.get("content") or []) < 3:
        return False
    if (slide.get("subtitle") or "").strip():
        return False
    title = slide.get("title") or ""
    if SKIP_RE.search(title.strip()):
        return False
    return True


