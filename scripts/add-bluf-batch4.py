total_added = 0
for deck, subtitles in DECKS:
    path = f"{BASE}/{deck}/slides-data.json"
    with open(path) as f:
        data = json.load(f)
    slides = data["slides"]
    added = 0
    for slide in slides:
        if not needs_subtitle(slide):
            continue
        title = slide.get("title") or ""
        if title in subtitles:
            slide["subtitle"] = subtitles[title]
            added += 1
    with open(path, "w") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        f.write("\n")
    print(f"{deck}: +{added} subtitles")
    total_added += added

